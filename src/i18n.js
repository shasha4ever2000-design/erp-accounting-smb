export const useI18n = create(
  persist(
    (set) => ({
      lang: 'en',
      setLang: (lang) => set({ lang }),
      toggle: () => set((s) => ({ lang: s.lang === 'ar' ? 'en' : 'ar' })),
    }),
    { name: 'erp-lang', version: 1 }
  )
)

export function useT() {
  const lang = useI18n((s) => s.lang)
  return (s) => (lang === 'ar' && AR[s]) ? AR[s] : s
}
